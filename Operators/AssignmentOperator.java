package Operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        System.out.println(a+=5);
        System.out.println(a-=5);
        System.out.println(a*=8);
        System.out.println(a/=8);
        System.out.println(a%=6);
    }
}
