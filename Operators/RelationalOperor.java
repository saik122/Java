package Operators;

public class RelationalOperor {
    public static void main(String[] args) {
        int a=20;
        int b=22;
        System.out.println(" a==b : "+ (a==b));
        System.out.println(" a>b : "+ (a>b));
        System.out.println(" a<b : "+ (a<b));
        System.out.println(" a>=b : "+ (a>=b));
        System.out.println(" a<=b : "+ (a<=b));
        System.out.println(" a!=b : "+ (a!=b));
    }
}
